        #region RandomGenerate
        public static string randomnumber(int[] arr, int m, int n)
        {
            Random random = new Random();
            string x = "";
            for (int i = 0; i < arr.Length; i++)
            {
                arr[i] = random.Next(m, n);
                x += arr[i] + "\t";
            }


            return x;
        }
